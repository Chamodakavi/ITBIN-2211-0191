    private void userdetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userdetailsMouseClicked

          // Get the existing table model from the JTable
  DefaultTableModel tableModel = (DefaultTableModel) userdetails.getModel();

    if (userdetails.getSelectedRow() != -1) {
        // Check if a row is selected
        
           int id = Integer.parseInt(tableModel.getValueAt(userdetails.getSelectedRow(), 0).toString());     
            String name = tableModel.getValueAt(userdetails.getSelectedRow(), 1).toString();
            String password = tableModel.getValueAt(userdetails.getSelectedRow(), 2).toString();

            
            txtname.setText(name);
            txtpassword.setText(password);
        }
        
        
    }//GEN-LAST:event_userdetailsMouseClicked

    private void txtsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsearchKeyReleased
        // Retrieve the search keyword from the text field
        ViewUser user = new ViewUser();
        user.setKeyword(txtsearch.getText());

        ViewUserControl controller = new ViewUserControl(this); // Assuming an instance creation mechanism
        controller.search(user.getKeyword());


            ViewUserDAO dao = new ViewUserDAO();
            List<String[]> userData= dao.search(user.getKeyword());

               DefaultTableModel model = (DefaultTableModel) userdetails.getModel();
            model.setRowCount(0); // Clear existing data

            for (String[] aa : userData) {
                model.addRow(aa);
            }
    
        
    }//GEN-LAST:event_txtsearchKeyReleased

