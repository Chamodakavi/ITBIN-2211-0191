    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        

        int selectedRow = userdetails.getSelectedRow();
        if (selectedRow >= 0) {
            
        int id = Integer.parseInt(userdetails.getValueAt(selectedRow, 0).toString());

        if (JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?", "Delete Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            // Send delete request to Controller (assuming communication mechanism)
            ViewUserControl controller = new ViewUserControl(this); // Assuming an instance creation mechanism
            controller.delete(id);
          
             formWindowOpened(null);
            
        }
    } else {
       
    }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        int id = Integer.parseInt(userdetails.getValueAt(userdetails.getSelectedRow(), 0).toString());
  
        
        ViewUser user = new ViewUser();
        user.setUserName(txtname.getText());
        user.setPassword(txtpassword.getText());
        user.setid(id);
        
        ViewUserControl controller = new ViewUserControl(this); // Assuming an instance creation mechanism

        
        controller.update(user);
        
        txtname.setText("");
        txtpassword.setText("");

        formWindowOpened(null);
    }//GEN-LAST:event_btnUpdateActionPerformed

